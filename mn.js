    //open
    function opo() {
        var x = document.getElementById("meno");
        if (x.style.display === "none") {
        x.style.display = "block";
        } else {
        x.style.display = "none";
        }
    }
    // close
    function colo() {
        var x = document.getElementById("meno");
        if (x.style.display === "none") {
        x.style.display = "block";
        } else {
        x.style.display = "none";
        }
    }


